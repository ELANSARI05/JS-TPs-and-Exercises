export default {
  name: "Register",
  data() {
    return {
      email: "",
      password: "",
      error: ""
    };
  },
  methods: {
    async register() {
      if (!this.email.endsWith('@um6p.ma')) {
        this.error = "Please use your UM6P email address.";
        return;
      }
      try {
        const userCredential = await projectAuth.createUserWithEmailAndPassword(this.email, this.password);
        const user = userCredential.user;
        this.$router.push("/home");
      } catch (error) {
        this.error = "Error creating account. Please try again.";
        console.error("Error registering:", error);
      }
    }
  }
};
